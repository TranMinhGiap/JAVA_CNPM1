    public void loadComboboxSupplier(){
        SanPhamRepository spReposytory = new SanPhamRepository();
        List<Supplier> lstSL = spReposytory.getcomboboxSupplier();
        cbxSupplier.removeAllItems();
        for(int i = 0 ; i < lstSL.size() ; i++){
            cbxSupplier.addItem(lstSL.get(i));
        }
    }
    public void loadComboboxCategory(){
        SanPhamRepository spReposytory = new SanPhamRepository();
        List<Category> lstSL = spReposytory.getComboboxCategory();
        cbxCategory.removeAllItems();
        for(int i = 0 ; i < lstSL.size() ; i++){
            cbxCategory.addItem(lstSL.get(i));
        }
    }
    
    public void showData(String id, String name, String idNCC, String idDM, float nhap, float ban, int num){
        txtID.setText(id);
        txtID.setEditable(false);
        txtName.setText(name);
        for(int i = 0 ; i < cbxSupplier.getItemCount() ; i++){
            Supplier sp = cbxSupplier.getItemAt(i);
            if(sp.getSupplierID().equals(idNCC)){
                cbxSupplier.setSelectedItem(sp);
            }
        }
        for(int i = 0 ; i < cbxCategory.getItemCount() ; i++){
            Category dm = cbxCategory.getItemAt(i);
            if(dm.getCategoryID().equals(idDM)){
                cbxCategory.setSelectedItem(dm);
            }
        }
        txtNhap.setText(String.valueOf(nhap));
        txtBan.setText(String.valueOf(ban));
        txtNum.setText(String.valueOf(num));
        txtNum.setEditable(false);
    }
    
    
