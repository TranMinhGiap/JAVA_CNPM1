    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        String name = txtName.getText();
        String priceNhap = txtNhap.getText();
        String priceBan = txtBan.getText();
        Category ctg = (Category)cbxCategory.getSelectedItem();
        Supplier sup = (Supplier)cbxSupplier.getSelectedItem();
        int num = 0;

        if(id.equals("") || name.equals("") || priceNhap.equals("") || priceBan.equals("")){
            JOptionPane.showMessageDialog(this, "PLEASE INPUT ALL DATA TO UPDATE PRODUCT !");
            return;
        }

        try {
            float tmp1 = Float.parseFloat(priceNhap);
            float tmp2 = Float.parseFloat(priceBan);
            if(tmp1 <= 0 || tmp2 <= 0){
                JOptionPane.showMessageDialog(this, "VALIDATE Price ! ");
                return;
            }
    
            Product pr = new Product(id, name, tmp1, tmp2, num, ctg.getCategoryID(), sup.getSupplierID());
            SanPhamRepository rp = new SanPhamRepository();
            if(rp.updateProduct(pr)){
                JOptionPane.showMessageDialog(this, "UPDATE Product SUCCESSFULL !");
            }
            else{
                JOptionPane.showMessageDialog(this, "UPDATE Product Failed !");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "VALIDATE TYPE Price ! " + ex.getMessage());
        }
        destroy();
    }//GEN-LAST:event_btnUpdateActionPerformed

