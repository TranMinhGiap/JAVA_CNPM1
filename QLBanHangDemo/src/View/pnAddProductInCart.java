    public void destroy(){
        Window window = SwingUtilities.windowForComponent(this);
        if (window != null) {
            window.dispose(); // Đóng JDialog
        }
    }
    void showDataProduct(Product pr, DefaultTableModel tbmCart){
        SanPhamRepository sprepo = new SanPhamRepository();
        this.lblShowIDSP.setText(pr.getProductID());
        this.lblShowThuongHieu.setText(sprepo.getNameSuppier(pr.getSupplierID()));
        this.txtGiaDangBan.setText(String.valueOf(pr.getProductPriceOut()) + " VND");
        this.txtTongTienChiTietHoaDon.setText(String.valueOf(pr.getProductPriceOut()) + "VND");
        this.pr = pr;
        this.tbmTmp = tbmCart;
        this.check = true;
    }
