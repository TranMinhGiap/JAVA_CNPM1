    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        SanPhamRepository sprepo = new SanPhamRepository();
        int value = (int) Spiner.getValue();  // SL MUA
        double price = (double)pr.getProductPriceOut();// GIA BAN TREN 1 SP
        String productType = sprepo.getNameCategory(pr.getCategoryID());
        String supplierName = sprepo.getNameSuppier(pr.getSupplierID());
        double total = (double)(price * value);
        if(value <= 0){
            JOptionPane.showMessageDialog(this, "Nhập Số Lượng Sản Phẩm Muốn Mua !");
            return;
        }
        try {
            tbmTmp.addRow(new Object[]{pr.getProductID(), pr.getProductName(), value, price, productType, supplierName, total});
            JOptionPane.showMessageDialog(this, "Đã Thêm Sản Phẩm Vào Giỏ Hàng !");
        } catch (Exception ex) {
            System.out.println("Error event btnAddToCart in pnAddProductInCart : " + ex.getMessage());
        }
        destroy();
    }//GEN-LAST:event_btnAddToCartActionPerformed

    private void SpinerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_SpinerStateChanged
        // TODO add your handling code here:
        if(check == true){
            int value = (int) Spiner.getValue();
            if(value > this.pr.getQuantity()){
//                System.out.println("value : pr.getQuantity : " + value + " : " + pr.getQuantity());
                JOptionPane.showMessageDialog(this, "Số Lượng Sản Phẩm Vượt Quá Giới Hạn");
                Spiner.setValue(pr.getQuantity());
            }
            double total = (double) this.pr.getProductPriceOut() * value;
            txtTongTienChiTietHoaDon.setText(String.valueOf(total) + " VNĐ");
        }
    }//GEN-LAST:event_SpinerStateChanged
